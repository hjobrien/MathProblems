package euclideanAlgorithm;

public class Value {
	private int number;
	private int c1;
	private int c2;

	public Value (int num, int c1, int c2){
		this.number = num;
		this.c1 = c1;
		this.c2 = c2;
	}

	public int getNumber() {
		return number;
	}

	public int getC1() {
		return c1;
	}

	public int getC2() {
		return c2;
	}
}
